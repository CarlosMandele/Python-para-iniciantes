# A função int recebe uma string e converte para um valor do tipo inteiro
a = int (input("Digite a primeira variavel:")) # 5 ---> "5"
b = int (input("Digite a segunda variavel:")) # 7 ---> "7"
x = float (input("Digite a terceira variavel:"))

soma = a + b #"5" + "7"
dobro = x * 3 # "2.5"

print(soma)
print(dobro)

