# Tipos de Dados Primitivos em Python


# Inteiro
idade = 20

#Float(Fracionarios)
altura = 1.75

# String(caractere)
saudacao = "Olá mundo!"

# Lógico(booleano)
aprovado = True

# Função type recebe como argumento uma variavel e informa qual o tipo esta armazenado na variavel
print(type(idade))
print(type(altura))
print(type(saudacao))
print(type(aprovado))
